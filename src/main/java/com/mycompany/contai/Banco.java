    private void btnCadastrarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarClienteActionPerformed

        // Obt√©m os valores dos campos de entrada
        String nome = inputNome.getText();
        String sobrenome = inputSobrenome.getText();
        String rg = inputRg.getText();
        String cpf = inputCpf.getText();
        String endereco = inputEndereco.getText();

        // Cria um novo cliente
        Cliente novoCliente = new Cliente(nome, sobrenome, rg, cpf, endereco);

        // Verifica se o cliente j√° existe na lista e, se n√£o, adiciona-o
        Sistema sistema = new Sistema(); // Normalmente voc√™ deve ter uma inst√¢ncia existente do Sistema

        if (!sistema.contemCliente(novoCliente)) {
            sistema.adicionarCliente(novoCliente);
            System.out.println("Cliente cadastrado com sucesso!");
        } else {
            System.out.println("Cliente ja cadastrado.");
        }
    }//GEN-LAST:event_btnCadastrarClienteActionPerformed

